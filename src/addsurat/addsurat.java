    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
         String filename = System.getProperty("user.home") + "\\Documents\\Debit.csv";


    try {
        // Membuat FileWriter untuk menulis data ke file
        FileWriter fw = new FileWriter(filename);

        // Mendapatkan model dari JTable
        TableModel model = table_user.getModel(); // Ganti `jTable1` dengan nama JTable Anda

        // Menulis header kolom ke file CSV
        for (int col = 0; col < model.getColumnCount(); col++) {
            fw.write(model.getColumnName(col) + (col < model.getColumnCount() - 1 ? "," : ""));
        }
        fw.write("\n"); // Ganti baris setelah header

        // Menulis data baris ke file CSV
        for (int row = 0; row < model.getRowCount(); row++) {
            for (int col = 0; col < model.getColumnCount(); col++) {
                fw.write(model.getValueAt(row, col).toString() + (col < model.getColumnCount() - 1 ? "," : ""));
            }
            fw.write("\n"); // Ganti baris setelah setiap baris data
        }

        // Menutup FileWriter
        fw.close();

        // Menampilkan pesan sukses
        JOptionPane.showMessageDialog(null, "Data berhasil diekspor ke: " + filename);
    } catch (Exception e) {
        // Menampilkan pesan error jika terjadi kesalahan
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace(); // Untuk debugging
    }
    
        
    }//GEN-LAST:event_jButton5ActionPerformed

