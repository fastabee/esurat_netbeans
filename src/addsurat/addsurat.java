                                 "', '" + keterangan.getText() + "', '" + addedby.getText() + "')";
            boolean b = s.execute(insertQuery);

            if (!b) {
                JOptionPane.showMessageDialog(null, "Berhasil tambah data");
                 nominal.setText("");
   tanggal_surat.setDate(null);
    keterangan.setText("");
                formWindowOpened(null);
            } else {
                JOptionPane.showMessageDialog(null, "Gagal tambah data");
            }
